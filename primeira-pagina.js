
function coracao(){

    const imagem = document.getElementById('coracao');
       
        if(imagem.src.match("./imagens/coracao-branco.png")){
            imagem.src = "./imagens/coracao-cheio1.png";
        
        }else{
            imagem.src = "./imagens/coracao-branco.png"
        }
}


